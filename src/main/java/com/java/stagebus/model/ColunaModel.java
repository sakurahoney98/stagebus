package com.java.stagebus.model;

public class ColunaModel {
	
	
	private static final String DEFAULT_VISOR = "NOME";
	private static final String DEFAULT_COLUNA = "COLUNA";
	

	
	
	private String visor = DEFAULT_VISOR;
	private String coluna = DEFAULT_COLUNA;
	
	
	public String getVisor() {
		return visor;
	}
	public void setVisor(String visor) {
		this.visor = visor;
	}
	public String getColuna() {
		return coluna;
	}
	public void setColuna(String coluna) {
		this.coluna = coluna;
	}
	
	
	
	
	

}
